<?php

//The array_pop() function deletes the last element of an array.
    $a=array("red","green","blue");
    array_pop($a);
    print_r($a);
    echo "<br>";

//The array_push() function inserts one or more elements to the end of an array.
    $a=array("red","green","blue");
    array_push($a,"pink","yellow");
    print_r($a);
?>